    /**Creates a user based on text fields when called.
     * 
     */
    private void createUser(){
        if(new String(jPasswordField1.getPassword()).equals(new String(jPasswordField2.getPassword()))){
                if(connection.createUser(jTextField2.getText(), jTextField1.getText(), new String(jPasswordField1.getPassword()))){
                    jLabel1.setText("The username already exists");
                    jLabel1.setVisible(true);
                }
                else{
                    connection.login(jTextField2.getText(), new String(jPasswordField1.getPassword()));
                    createUserFrame.dispatchEvent(new java.awt.event.WindowEvent(createUserFrame, java.awt.event.WindowEvent.WINDOW_CLOSING));
                    new MainFrame(connection, 1);
                }
            }
            else{
                jLabel1.setText("The passwords did not match.");
                jLabel1.setVisible(true);
            }
    }

